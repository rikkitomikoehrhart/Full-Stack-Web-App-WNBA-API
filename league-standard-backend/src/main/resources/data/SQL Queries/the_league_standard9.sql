CREATE TABLE player_season_stats (
    id INT AUTO_INCREMENT PRIMARY KEY,
    player_id VARCHAR(100) NOT NULL,
    games_played INT,
    games_started INT,
    minutes INT,
    field_goals_made INT,
    field_goals_pct DECIMAL(4,1) DEFAULT 0.0,
    two_points_made INT,
    two_points_pct DECIMAL(4,1) DEFAULT 0.0,
    three_points_made INT,
    three_points_pct DECIMAL(4,1) DEFAULT 0.0,
    free_throws_made INT,
    free_throws_pct DECIMAL(4,1) DEFAULT 0.0,
    offensive_rebounds INT,
    defensive_rebounds INT,
    assists INT,
    turnovers INT,
    steals INT,
    blocks INT, 
    personal_fouls INT,
    tech_fouls INT,
    points INT,
    flagrant_fouls INT,
    fouls_drawn INT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    
    FOREIGN KEY (player_id) REFERENCES players(id) ON DELETE CASCADE,
    UNIQUE KEY unique_game_boxscore (player_id)
);